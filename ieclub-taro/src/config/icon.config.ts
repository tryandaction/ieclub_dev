// 图标组件辅助函数
export const getIcon = (category: keyof typeof IconConfig, name: string): string => {
  const categoryIcons = IconConfig[category] as Record<string, string>
  return categoryIcons[name] || 'mdi:help-circle'
}

// 获取话题类型图标
export const getTopicTypeIcon = (type: string): string => {
  switch (type) {
    case 'topic_offer':
      return IconConfig.topic.offer
    case 'topic_demand':
      return IconConfig.topic.demand
    case 'project':
      return IconConfig.topic.project
    default:
      return IconConfig.topic.discussion
  }
}

// 获取话题类型颜色
export const getTopicTypeColor = (type: string): string => {
  switch (type) {
    case 'topic_offer':
      return '#5B7FFF' // 蓝色
    case 'topic_demand':
      return '#FF6B9D' // 粉色
    case 'project':
      return '#FFA500' // 橙色
    default:
      return '#999999' // 灰色
  }
}

// 获取话题类型文本
export const getTopicTypeText = (type: string): string => {
  switch (type) {
    case 'topic_offer':
      return '我来讲'
    case 'topic_demand':
      return '想听'
    case 'project':
      return '项目'
    default:
      return '话题'
  }
}

